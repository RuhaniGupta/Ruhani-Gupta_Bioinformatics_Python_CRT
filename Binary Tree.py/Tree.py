'''A tree is a data structure similar to a linked list,but instead of each node pointing simply to the next
node in a linear fashion,each node points to several nodes.
A tree is an example of a non-linear data strucutre.
A tree structure is a way of representing the hierarchial nature of a structure in a graphical form.'''

'''
The root of a tree is the node with no parent.There can be at most  one root node in a tree.
An edge refer to the link from a parent to a child.
A node with no children is called a leaf node.
The children node of the same parent are called siblings.'''

'''
The set of all nodes at a given depth is called a  level of the tree.The root node is at level 0.
The depth of a node is a length of the path from the root to the node.
The height of a node is the length of the path from that node at the deepest node.
The height of a tree is the length of the path from the root to the deepest node in the tree.
A(rooted) tree with only one node(the root) has a height of 0.'''


